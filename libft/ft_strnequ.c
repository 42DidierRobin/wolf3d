#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t i;

	if (!s2 || !s1)
		return (0);
	i = 0;
	while (s1[i] == s2[i] && s1[i] && (i < n))
	{
		i++;
		if (s1[i] == s2[i] && (!s1[i] || i == (n - 1)))
			return (1);
	}
	if ((n == 0) || (s1[i] == s2[i] && !s1[i]))
		return (1);
	return (0);
}
